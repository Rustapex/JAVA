package variable;
//자바 파일을 구분하기 위한 폴더로 이해

public class Var1 {
    public static void main(String[] args) {
        System.out.println(10); //변경 10 -> 20
        System.out.println(10);
        System.out.println(10);

        int num = 20;
        System.out.println(num);
        System.out.println(num);
        System.out.println(num);

    }
}
